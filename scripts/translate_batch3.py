# 2. Avadhuta Upanishad
avadhuta_content = """ಗೌಣಮುಖ್ಯಾವಧೂತಾಲಿಹೃದಯಾಂಬುಜವರ್ತಿ ಯತ್ ।
ತತ್ತ್ರೈಪದಂ ಬ್ರಹ್ಮತತ್ತ್ವಂ ಸ್ವಮಾತ್ರಮವಶಿಷ್ಯತೇ  ॥
ಓಂ ಸಹ ನಾವವತು ಸಹ ನೌ ಭುನಕ್ತು ಸಹ ವೀರ್ಯಂ ಕರವಾವಹೈ ।
ತೇಜಸ್ವಿನಾವಧೀತಮಸ್ತು ಮಾ ವಿದ್ವಿಷಾವಹೈ  ॥
ಓಂ ಶಾಂತಿಃ ಶಾಂತಿಃ ಶಾಂತಿಃ  ॥

==================================================

ಅಥ ಹ ಸಾಂಕೃತಿರ್ಭಗವಂತಮವಧೂತಂ ದತ್ತಾತ್ರೇಯಂ ಪರಿಸಮೇತ್ಯ ಪಪ್ರಚ್ಛ ಭಗವನ್ಕೋಽವಧೂತಸ್ಯ ಕಾ ಸ್ಥಿತಿಃ ಕಿಂ ಲಕ್ಷ್ಮ ಕಿಂ ಸಂಸರಣಮಿತಿ ।
ತಂ ಹೋವಾಚ ಭಗವೋ ದತ್ತಾತ್ರೇಯಃ ಪರಮಕಾರುಣಿಕಃ ॥

ಭಾವಾರ್ಥ:
ಒಮ್ಮೆ ಸಾಂಕೃತಿ ಋಷಿಯು ಪರಮ ದಯಾಳುವಾದ ಅವಧೂತ ದತ್ತಾತ್ರೇಯ ಭಗವಂತನ ಬಳಿ ಬಂದು ಕೇಳಿದನು: "ಭಗವಂತನೇ, ಅವಧೂತನ ಸ್ಥಿತಿ ಏನು? ಅವನ ಲಕ್ಷಣಗಳೇನು? ಅವನು ಸಂಸಾರದಲ್ಲಿ ಹೇಗೆ ಸಂಚರಿಸುತ್ತಾನೆ?" ಅದಕ್ಕೆ ಪರಮ ಕಾರುಣಿಕನಾದ ದತ್ತಾತ್ರೇಯನು ಈ ರೀತಿ ಉತ್ತರಿಸಿದನು.

==================================================

ಅಕ್ಷರತ್ವಾದ್ವರೇಣ್ಯತ್ವಾದ್ಧೃತಸಂಸಾರಬಂಧನಾತ್ ।
ತತ್ತ್ವಮಸ್ಯಾದಿಲಕ್ಷ್ಯತ್ವಾದವಧೂತ ಇತೀರ್ಯತೇ  ॥

ಶಬ್ದಾರ್ಥ:
• ಅಕ್ಷರತ್ವಾತ್ - ಅಖಂಡ ನಾಶರಹಿತವಾದ ಬ್ರಹ್ಮಭಾವದಲ್ಲಿ ಸದಾ ಇರುವುದರಿಂದ (ಅ);
• ವರೇಣ್ಯತ್ವಾತ್ - ಸಕಲ ಸದ್ಗುಣಗಳಿಂದ ಕೂಡಿದ ಶ್ರೇಷ್ಠತೆಯಿಂದ (ವ);
• ಧೃತ ಸಂಸಾರ ಬಂಧನಾತ್ - ಸಂಸಾರ ಬಂಧನವನ್ನು ಕೊಡವಿದವನಾಗಿರುವುದರಿಂದ (ಧೂ);
• ತತ್ತ್ವಮಸ್ಯಾದಿ ಲಕ್ಷ್ಯತ್ವಾತ್ - 'ತತ್ತ್ವಮಸಿ' ಮಹಾವಾಕ್ಯದ ಲಕ್ಷ್ಯಾರ್ಥವಾದ ಪರಬ್ರಹ್ಮಸ್ವರೂಪವೇ ಆಗಿರುವುದರಿಂದ (ತ);
• ಅವಧೂತಃ ಇತಿ ಈರ್ಯತೇ - ಅವನನ್ನು 'ಅವಧೂತ' ಎಂದು ಕರೆಯಲಾಗುತ್ತದೆ;

ಭಾವಾರ್ಥ:
ಅವಧೂತ ಎಂಬ ಶಬ್ದವು ನಾಲ್ಕು ದಿವ್ಯ ಗುಣಗಳನ್ನು ಸೂಚಿಸುತ್ತದೆ: ಅಕ್ಷರತ್ವ (ನಾಶರಹಿತ ಸ್ಥಿತಿ), ವರೇಣ್ಯತ್ವ (ಶ್ರೇಷ್ಠತೆ), ಧೂತಸಂಸಾರಬಂಧನ (ಬಂಧನಗಳ ತ್ಯಾಗ) ಮತ್ತು ತತ್ತ್ವಮಸ್ಯಾದಿ ಮಹಾವಾಕ್ಯಗಳ ಸಾಕ್ಷಾತ್ಕಾರ. ಈ ಸ್ಥಿತಿಯಲ್ಲಿರುವವನೇ ಅವಧೂತನು.

==================================================

(ವಿವರಣೆ: ಈ ಉಪನಿಷತ್ತಿನಲ್ಲಿ ಕರ್ಮಕಾಂಡವನ್ನು ಮೀರಿ ನಿಂತಿರುವ ಜ್ಞಾನಯೋಗಿಯ ದಿವ್ಯಾವಸ್ಥೆಯನ್ನು ನಿರೂಪಿಸಲಾಗಿದೆ.)"""

