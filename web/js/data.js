// ── Helper Functions ───────────────────────────────────────
function getAllShlokas() {
  const shlokas = [];
  APP_DATA.books.forEach((book) => {
    book.chapters.forEach((chapter) => {
      chapter.shlokas.forEach((shloka) => {
        shlokas.push({
          ...shloka,
          bookId: book.id,
          bookTitle: book.title,
          bookTitleEn: book.titleEn,
          chapterId: chapter.id,
          chapterTitle: chapter.title,
          chapterNumber: chapter.number,
          category: book.category,
          subcategory: book.subcategory,
        });
      });
    });
  });
  return shlokas;
}

function getBooksByCategory(categoryId) {
  return APP_DATA.books.filter((b) => b.category === categoryId);
}

function getBooksBySubcategory(subcategoryId) {
  return APP_DATA.books.filter((b) => b.subcategory === subcategoryId);
}

function getBooksByGod(godId) {
  return APP_DATA.books.filter(
    (b) => b.godRelated && b.godRelated.includes(godId)
  );
}

function getBookById(bookId) {
  return APP_DATA.books.find((b) => b.id === bookId);
}

function searchShlokas(query) {
  const q = query.toLowerCase();
  const results = [];
  APP_DATA.books.forEach((book) => {
    book.chapters.forEach((chapter) => {
      chapter.shlokas.forEach((shloka) => {
        if (
          shloka.sanskrit.toLowerCase().includes(q) ||
          shloka.kannada.toLowerCase().includes(q) ||
          shloka.meaning.toLowerCase().includes(q) ||
          (shloka.explanation && shloka.explanation.toLowerCase().includes(q)) ||
          book.title.toLowerCase().includes(q) ||
          book.titleEn.toLowerCase().includes(q)
        ) {
          results.push({
            ...shloka,
            bookId: book.id,
            bookTitle: book.title,
            bookTitleEn: book.titleEn,
            chapterTitle: chapter.title,
          });
        }
      });
    });
  });
  return results;
}
